    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String usuarioTxt = txtUsuario.getText().trim();
    String claveTxt   = new String(txtPassword.getPassword()).trim(); // si usas JPasswordField

    if (usuarioTxt.isEmpty() || claveTxt.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Ingresa usuario y contraseña.",
            "Datos incompletos",
            javax.swing.JOptionPane.WARNING_MESSAGE);
        return;
    }

    // 1) Consultar en BD
    Usuario u = usuarioDAO.buscarPorUsuarioYClave(usuarioTxt, claveTxt);

    if (u == null) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Usuario o contraseña incorrectos.",
            "Acceso denegado",
            javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 2) Login correcto
    String nombreCliente = u.getUsername();  // por ahora usamos username
    String rol = u.getRol();                 // ADMIN o USUARIO

    // 3) Abrir ventana según rol
    if ("ADMIN".equalsIgnoreCase(rol)) {
        AdminInventarioFrame admin = new AdminInventarioFrame();
        admin.setVisible(true);
    } else {
        usuarioCompra2 frm = new usuarioCompra2(nombreCliente);
        frm.setVisible(true);
    }

    // 4) Cerrar login
    this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

