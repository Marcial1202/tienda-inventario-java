    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
    String usuario = txtUsuario.getText().trim();
    String password = new String(txtPassword.getPassword()).trim();

    // Validar campos vacíos
    if (usuario.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this,
                "Ingresa usuario y contraseña",
                "Campos incompletos",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    // ======= AQUI VA SOLO LA VALIDACIÓN CON BD =======
    UsuarioDAO dao = new UsuarioDAO();
   Usuario u = dao.buscarPorUsuarioYClave(usuario, password);

    if (u != null) {
        // Login OK
        JOptionPane.showMessageDialog(this,
                "¡Bienvenido " + u.getUsername() + " (" + u.getRol() + ")!",
                "Login exitoso",
                JOptionPane.INFORMATION_MESSAGE);

        // Abrir ventana según el rol
        if ("ADMIN".equalsIgnoreCase(u.getRol())) {
            administradores adminFrame = new administradores();
            centrarVentana(adminFrame);
            adminFrame.setVisible(true);
        } else {
            usuarioCompra compraFrame = new usuarioCompra();
            centrarVentana(compraFrame);
            compraFrame.setVisible(true);
        }

        // Cerrar login
        this.dispose();
    } else {
        // No encontró usuario en la BD
        JOptionPane.showMessageDialog(this,
                "Usuario o contraseña incorrectos",
                "Error de login",
                JOptionPane.ERROR_MESSAGE);
        txtPassword.setText("");
        txtUsuario.requestFocus();
    }
}

