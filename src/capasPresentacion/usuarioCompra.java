private void cargarProductos() {
        ProductoDAO productoDAO = new ProductoDAO();
       listaProductos = productoDAO.listarTodos(); // Guardar la lista completa

        List<Producto> productos = productoDAO.listarTodos(); // Obtener lista de productos desde la base de datos
        
        // Crear un modelo para el comboBox
        DefaultComboBoxModel<String> modeloCombo = new DefaultComboBoxModel<>();
        
        // Llenar el comboBox con los nombres de los productos
        for (Producto producto : productos) {
            modeloCombo.addElement(producto.getNombre()); // Se agrega el nombre del producto
        }

        // Establecer el modelo al JComboBox
        cmbProducto.setModel(modeloCombo);
    }
